    private void buttonLogarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonLogarActionPerformed
        String p_user = inputUser.getText();
        String p_pass = inputPass.getText();
        
        if(p_user.equals("") || p_pass.equals("")){
            status.setForeground(Color.red);
            status.setText("Preencha os campos vazios!");
            return;
        }
        
        for(int i=0; i<=dados.position;i++){
            if(p_user.equals(dados.user[i]) && p_pass.equals(dados.password[i])){
                status.setForeground(Color.green);
                status.setText("Acesso permitido!");
                return;
            }
        }
        status.setForeground(Color.red);
        status.setText("Senha ou usuÃ¡rio incorretos!");
    }//GEN-LAST:event_buttonLogarActionPerformed

